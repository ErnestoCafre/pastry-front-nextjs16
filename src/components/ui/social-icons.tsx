const socialIcons: Record<SocialPlatform, React.ComponentType<IconProps>> = {
  whatsapp: WhatsAppIcon,
  instagram: InstagramIcon,
  facebook: FacebookIcon,
  tiktok: TikTokIcon,
  twitter: TwitterIcon,
  youtube: YouTubeIcon,
  linkedin: LinkedInIcon,
  pinterest: PinterestIcon,
  telegram: TelegramIcon,
  other: OtherIcon,
}

const socialPlatformLabels: Record<SocialPlatform, string> = {
  whatsapp: 'WhatsApp',
  instagram: 'Instagram',
  facebook: 'Facebook',
  tiktok: 'TikTok',
  twitter: 'X (Twitter)',
  youtube: 'YouTube',
  linkedin: 'LinkedIn',
  pinterest: 'Pinterest',
  telegram: 'Telegram',
  other: 'Enlace',
}

interface SocialIconProps extends IconProps {
  platform: string
}

export function SocialIcon({ platform, className, ...props }: SocialIconProps) {
  const normalizedPlatform = platform.toLowerCase() as SocialPlatform
  const Icon = socialIcons[normalizedPlatform] || socialIcons.other
  return <Icon className={className} {...props} />
}

export function getSocialPlatformLabel(platform: string): string {
  const normalizedPlatform = platform.toLowerCase() as SocialPlatform
  return socialPlatformLabels[normalizedPlatform] || platform
}
